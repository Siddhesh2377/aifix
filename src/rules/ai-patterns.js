/**
 * Detects common AI-specific code patterns that indicate
 * generated code that wasn't reviewed.
 */
module.exports = {
  id: "ai-code-patterns",
  name: "AI-generated code patterns",
  severity: "warning",

  check(lines, filePath) {
    const issues = [];

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      const trimmed = line.trim();

      // "As an AI" or similar AI disclosure in comments/strings
      if (/as an ai|i'm an ai|ai.?generated|generated by (?:chatgpt|copilot|claude|gpt)/i.test(line)) {
        issues.push({
          rule: this.id,
          line: i + 1,
          column: 1,
          message: "AI disclosure left in code — remove before shipping",
          severity: "warning",
        });
      }

      // Overly generic variable names that AI loves
      if (/\b(?:const|let|var)\s+(data|result|response|output|temp|tmp|value|item|element|obj)\s*=/.test(line)) {
        // Only flag if there are multiple in the same file
        const genericCount = lines.filter(l =>
          /\b(?:const|let|var)\s+(data|result|response|output|temp|tmp)\s*=/.test(l)
        ).length;
        if (genericCount >= 3 && i === lines.findIndex(l => /\b(?:const|let|var)\s+(data|result|response|output|temp|tmp)\s*=/.test(l))) {
          issues.push({
            rule: this.id,
            line: i + 1,
            column: 1,
            message: `${genericCount} generic variable names (data, result, temp...) — use descriptive names`,
            severity: "info",
          });
        }
      }

      // Redundant type conversions AI loves to add
      if (/String\(.*\.toString\(\)\)/.test(line) || /Number\(parseInt\(/.test(line) || /Boolean\(!!/.test(line)) {
        issues.push({
          rule: this.id,
          line: i + 1,
          column: 1,
          message: "Redundant type conversion — pick one method",
          severity: "info",
        });
      }

      // AI often generates === undefined && === null chains instead of == null
      if (/===?\s*undefined\s*(\|\||&&)\s*.*===?\s*null/.test(line) || /===?\s*null\s*(\|\||&&)\s*.*===?\s*undefined/.test(line)) {
        issues.push({
          rule: this.id,
          line: i + 1,
          column: 1,
          message: "Verbose null check — use `== null` to check both null and undefined",
          severity: "info",
        });
      }
    }

    return issues;
  },
};
